public class IfElseDemo {

    public static void main(String[] args) {

        int age = 16;

        System.out.println("===== if-else Statement =====\n");

        if (age >= 18) {
            System.out.println("Adult");
        } else {
            System.out.println("Minor");
        }

        System.out.println("Program Ended.");
    }
}