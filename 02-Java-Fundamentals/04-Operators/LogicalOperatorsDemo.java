public class LogicalOperatorsDemo {

    public static void main(String[] args) {

        boolean a = true;
        boolean b = false;

        System.out.println("===== Logical Operators =====\n");

        System.out.println("a && b : " + (a && b));
        System.out.println("a || b : " + (a || b));
        System.out.println("!a     : " + (!a));
        System.out.println("!b     : " + (!b));
    }
}